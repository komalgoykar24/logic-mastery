public class NVERTEDHALFPYRAMIDwithNumbers {
    public static void find(){
        int k=5;
        for(int i=1;i<=5;i++){
            for(int j=1;j<=k;j++)
            {
                System.out.print(j+" ");
            }
            k--;
            System.out.println();
        }
    }
    public static void main(String[] args) {
    find();
    }
}
